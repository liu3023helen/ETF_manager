def main() -> None:
    conn = sqlite3.connect(DB_PATH)
    cur = conn.cursor()

    # 删除旧表并按文档重建
    cur.execute("DROP TABLE IF EXISTS trading_rules")

    cur.execute(
        """
        CREATE TABLE trading_rules (
            rule_id INTEGER PRIMARY KEY AUTOINCREMENT,
            fund_category TEXT,
            rule_type TEXT,
            condition_desc TEXT,
            threshold REAL,
            action_desc TEXT,
            priority INTEGER,
            is_active INTEGER,
            created_at TEXT,
            updated_at TEXT
        )
        """
    )

    # 建索引（与数据库结构文档保持一致）
    cur.execute("CREATE INDEX idx_trading_rules_fund_category ON trading_rules(fund_category)")
    cur.execute("CREATE INDEX idx_trading_rules_rule_type ON trading_rules(rule_type)")
    cur.execute("CREATE INDEX idx_trading_rules_is_active ON trading_rules(is_active)")

    now = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    insert_sql = (
        "INSERT INTO trading_rules "
        "(fund_category, rule_type, condition_desc, threshold, action_desc, "
        " priority, is_active, created_at, updated_at) "
        "VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)"
    )

    rows = [
        (fc, rt, cond, thr, act, prio, active, now, now)
        for (fc, rt, cond, thr, act, prio, active) in rules_data
    ]

    cur.executemany(insert_sql, rows)
    conn.commit()

    print(f"重新创建 trading_rules 表并插入 {len(rows)} 条规则。")

    conn.close()


if __name__ == "__main__":
    main()
