def main() -> None:
    conn = sqlite3.connect(DB_PATH)
    cur = conn.cursor()

    cur.execute("DROP TABLE IF EXISTS fund_holdings")
    cur.execute(
        """
        CREATE TABLE fund_holdings (
            fund_code TEXT PRIMARY KEY,
            fund_name TEXT,
            fund_company TEXT,
            fund_type TEXT,
            tracking_index TEXT,
            risk_level TEXT,
            fund_category TEXT,
            return_1y TEXT,
            return_3y TEXT,
            return_since_inception TEXT,
            created_at TEXT,
            updated_at TEXT,
            top_holdings TEXT,
            risk_points TEXT
        )
        """
    )

